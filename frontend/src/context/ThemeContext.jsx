const ThemeCtx = createContext(null)

export function ThemeProvider({ children }) {
  const [theme, setTheme] = useState(() => localStorage.getItem('aerotf-theme') || 'canvas')
  const [font, setFont] = useState(() => localStorage.getItem('aerotf-font') || 'public')

  useEffect(() => {
    const root = document.documentElement
    const selected = themes[theme] ?? themes.canvas
    Object.entries(selected.vars).forEach(([key, value]) => root.style.setProperty(key, value))
    root.dataset.theme = theme
    document.body.dataset.theme = theme
    localStorage.setItem('aerotf-theme', theme)
  }, [theme])

  useEffect(() => {
    const root = document.documentElement
    const selected = fonts[font] ?? fonts.public
    Object.entries(selected.vars).forEach(([key, value]) => root.style.setProperty(key, value))
    root.dataset.font = font
    localStorage.setItem('aerotf-font', font)
  }, [font])

  return (
    <ThemeCtx.Provider value={{ theme, setTheme, font, setFont }}>
      {children}
    </ThemeCtx.Provider>
  )
}

export const useTheme = () => useContext(ThemeCtx)
